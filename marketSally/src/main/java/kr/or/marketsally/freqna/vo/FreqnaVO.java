package kr.or.marketsally.freqna.vo;

public class FreqnaVO {
	private String freq_id    ;
	private String freq_cate  ;
	private String freq_cont  ;
	private String mem_id     ;
	private String freq_title ;
	private String freq_date  ;
	
	public String getFreq_id() {
		return freq_id;
	}
	public void setFreq_id(String freq_id) {
		this.freq_id = freq_id;
	}
	public String getFreq_cate() {
		return freq_cate;
	}
	public void setFreq_cate(String freq_cate) {
		this.freq_cate = freq_cate;
	}
	public String getFreq_cont() {
		return freq_cont;
	}
	public void setFreq_cont(String freq_cont) {
		this.freq_cont = freq_cont;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getFreq_title() {
		return freq_title;
	}
	public void setFreq_title(String freq_title) {
		this.freq_title = freq_title;
	}
	public String getFreq_date() {
		return freq_date;
	}
	public void setFreq_date(String freq_date) {
		this.freq_date = freq_date;
	}
	
}
