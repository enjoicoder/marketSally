package kr.or.marketsally.product.vo;

public class ProdVO {
	
	private String prod_id;
	private String prod_name;
	private int prod_price;
	private String prod_img;
	private String prod_cont;
	private String lprod_id;
	private String prod_date;
	private String lprod_name;
	
	public String getLprod_name() {
		return lprod_name;
	}
	public void setLprod_name(String lprod_name) {
		this.lprod_name = lprod_name;
	}
	public String getProd_id() {
		return prod_id;
	}
	public void setProd_id(String prod_id) {
		this.prod_id = prod_id;
	}
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public int getProd_price() {
		return prod_price;
	}
	public void setProd_price(int prod_price) {
		this.prod_price = prod_price;
	}
	public String getProd_img() {
		return prod_img;
	}
	public void setProd_img(String prod_img) {
		this.prod_img = prod_img;
	}
	public String getProd_cont() {
		return prod_cont;
	}
	public void setProd_cont(String prod_cont) {
		this.prod_cont = prod_cont;
	}
	public String getLprod_id() {
		return lprod_id;
	}
	public void setLprod_id(String lprod_id) {
		this.lprod_id = lprod_id;
	}
	public String getProd_date() {
		return prod_date;
	}
	public void setProd_date(String prod_date) {
		this.prod_date = prod_date;
	}

}
